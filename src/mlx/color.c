#include "../include/fractol.h"

int	get_color(int i)
{
	int	r;
	int	g;
	int	b;

	if (i == MAX_ITER)
		return (0x000000);
	r = (i * 9) % 256;
	g = (i * 2) % 256;
	b = (i * 4) % 256;
	return ((r << 16) | (g << 8) | b);
}
